package dev.yasmramos.pulsefx.navigation;

/**
 * Enumeration of available transition types for navigation animations.
 */
public enum TransitionType {
    /**
     * No animation, immediate view change.
     */
    NONE,
    
    /**
     * Fade in/out animation.
     */
    FADE,
    
    /**
     * Slide left/right animation.
     */
    SLIDE
}
